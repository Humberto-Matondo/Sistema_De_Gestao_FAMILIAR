    private void adc_valoresPouActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adc_valoresPouActionPerformed
        // TODO add your handling code here:
        
        String v= valorPou.getText();
        String motivoP= motivoPou.getText();
        String dataP= dataPou.getText();        
        float valorP = Float.parseFloat(v);
        float desc1;
    try {
        desc1 = PrevisaoBD.descontos1(idpessoa) + PrevisaoBD.somaPrev(idfamilia) + PrevisaoBD.descontos2(idfamilia);
        Login.saldo= RendimentoBD.somaRendimentos(idpessoa)-(desc1);
    } catch (SQLException ex) {
        Logger.getLogger(Adc_Valores_Pou.class.getName()).log(Level.SEVERE, null, ex);
    }   
        if(valorP<=Login.saldo){
            Poupanca p=new Poupanca(motivoP, valorP, dataP);
            PoupancaBD pou = new PoupancaBD();
            Familia f = new Familia(null, null, null, null);
            Login.saldo=Login.saldo-valorP;
            f.setIdFamilia(idfamilia);
            p.setFamilia(f);
            pou.adicionarPoupanca(p);
            dispose();
            new Poupancas().setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null, "Valor da PoupanÃ§a superior ao rendimento");
        }
    }//GEN-LAST:event_adc_valoresPouActionPerformed

